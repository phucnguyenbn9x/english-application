    /**
     * initial list of learned Word in scroll panel
     *
     * @param listWord list of words
     */
    public void initLearnedWords(ArrayList<Word> listWord) {
        DefaultListModel<Word> listModel = wordController.createListModel(listWord);
        JList<Word> wordsList = new JList<Word>(listModel);
        wordsList.setFont(new Font("Dialog", Font.PLAIN, 12));
        wordsList.setCellRenderer(new DefaultListCellRenderer() {
            @Override
            public Component getListCellRendererComponent(JList<?> list, Object value, int index, boolean isSelected, boolean cellHasFocus) {
                Component renderer = super.getListCellRendererComponent(list, value, index, isSelected, cellHasFocus);
                if (renderer instanceof JLabel && value instanceof Word) {
                    ((JLabel) renderer).setText(((Word) value).getName());
                }
                return renderer;
            }

        });
        wordsList.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                Word selectedWord = wordsList.getSelectedValue();
                if (selectedWord != null) {
                    jMeaning.setText(selectedWord.getMeaning());
                    jPronounce.setText(selectedWord.getPronounciation());
                    jLearnProgress.setValue(selectedWord.getLearnProgress() * 20);
                    wordController.createImage(selectedWord.getWordImage(), jImage);
                }
            }
        });
        jLearnedWords.setViewportView(wordsList);
    }

    /**
     * setting up data for this dialog
     *
     * @param listWord list words for this dialog
     */
    public void setupData(ArrayList<Word> listWord) {
        this.learnedWords = listWord;
        this.initLearnedWords(listWord);
    }

