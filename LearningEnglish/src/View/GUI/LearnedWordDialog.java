    /**
     * event when keyboard type in filter field
     *
     * @param evt event fired
     */
    private void jFilterKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFilterKeyTyped
        String input = jFilter.getText() + ((int) evt.getKeyChar() != 8 ? evt.getKeyChar() : "");
        ArrayList<Word> matchesWord = new ArrayList<Word>();
        for (Word word : this.learnedWords) {
            if (word.getName().regionMatches(true, 0, input, 0, input.length())) {
                matchesWord.add(word);
            }
        }
        this.initLearnedWords(matchesWord);
    }//GEN-LAST:event_jFilterKeyTyped

